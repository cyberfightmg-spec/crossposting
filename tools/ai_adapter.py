import os
import httpx
from dotenv import load_dotenv
load_dotenv()

OPENAI_API_KEY = os.getenv("OPENAI_API_KEY")
GEMINI_API_KEY = os.getenv("GEMINI_API_KEY")


VK_SYSTEM_PROMPT = """# Задача
Адаптировать пост из Telegram для публикации в ВКонтакте
и добавить блок хэштегов в конце.

# Вход

## Исходный пост (Telegram)
<<<
{tg_post}
>>>

## Настройки паблика ВК
- Аудитория: смешанная, не обязательно в теме
- Жаргон: пояснять
- Фирменный тег сообщества: #leaduxai
- Ниша паблика: нейросети, AI-контент, вайбкодинг

# Пошаговая инструкция

## Шаг 1. Что сохранить без изменений
- Голос автора и интонацию (первое лицо, разговорный тон).
- Все факты, цифры, имена, бренды, названия инструментов.
- Логику и порядок повествования.
- Клиффхэнгер или вопрос в конце, если есть.

## Шаг 2. Переделать хук (первые 2-3 строки)
ВК на мобильном обрезает пост на "Показать полностью" примерно
на 200-300 знаках. Первые 2-3 строки должны работать как
самостоятельный тизер.

Запрещённые начала:
- "Моя история началась с..."
- "Сегодня хочу рассказать..."
- "Давно хотел поделиться..."
- Любое начало с медленного разгона.

Рекомендуемые начала:
- Сразу сцена или конкретика.
- Интрига с конкретным фактом или цифрой.
- Короткий спорный тезис.

## Шаг 3. Убрать Markdown
ВК не рендерит разметку. Удалить или заменить:
- **текст** и __текст__ → обычный текст
- `код` и ```блоки``` → обычный текст или кавычки
- # Заголовки → обычная строка или убрать
- > цитаты → обычный абзац или кавычки
- [текст](ссылка) → текст, ссылка отдельной строкой

## Шаг 4. Разрывы строк
- Убрать одинарные переносы посреди мысли.
- Между абзацами — пустая строка.
- Абзац — законченный смысловой кусок.

## Шаг 5. Почистить рыхлости
Вырезать без замены:
- "честно", "если честно"
- "как это обычно бывает"
- "повторюсь"
- "в какой-то момент" (заменить на конкретику)
- "и вот"
- "но, как говорится"
- "не секрет, что"
- "в общем", "короче" в начале предложения
- "на самом деле" как вводное

## Шаг 6. Склеить рваные предложения
Короткие обрывки из 2-3 слов объединить в полные фразы,
если не ломает ритм и авторский акцент.

## Шаг 7. Жаркон
- "пояснять": первое употребление термина раскрыть в 3-6 слов.
- "сохранять": ничего не пояснять.
- "упрощать": заменить термин бытовым аналогом везде.

## Шаг 8. Правила ВК
Не пропускать: накрутки, обещания заработка, оскорбления,
запрещённые тематики. Пограничное — переформулировать.
Если невозможно — пометить после ---.

## Шаг 9. Хэштеги в конце поста

### Структура блока тегов
Блок идёт последним, отделён от текста одной пустой строкой.
Сами теги — одной строкой через пробел. Всего 5-8 штук.

### Состав
1. Фирменный тег сообщества (если указан в настройках) — 1 шт,
   всегда первым. Пример: #leaduxai

2. Широкие популярные теги по теме поста — 2-3 шт.
   Это общеупотребимые теги, под которыми в ВК большой объём
   контента и реальные подписки/поиск.
   Выбирать по теме конкретного поста, не по нише паблика в целом.
   Примеры для AI/нейросетей: #нейросети #искусственныйинтеллект
   #ии #AI #ChatGPT #нейросеть
   Примеры для бизнеса: #бизнес #маркетинг #предприниматель
   Примеры для личной истории про путь: #мояистория #опыт #путь

3. Узкие тематические теги — 2-3 шт.
   Конкретнее, ближе к сути поста. Сюда идут названия инструментов,
   методов, специфические термины.
   Примеры: #вайбкодинг #claudecode #cursor #нейромонтаж
   #aiвидео #промптинг

4. Опционально — 1 тег с гео или форматом, если уместно.
   Примеры: #работаснейросетями #историяуспеха #доиспосле

### Правила написания
- Все теги с маленькой буквы, кроме аббревиатур и названий брендов
  (#ChatGPT, #AI, #SMM — можно с большой).
- Без пробелов внутри тега: #мояистория, не #моя история.
- Без эмодзи и спецсимволов внутри.
- Кириллица и латиница — как принято для конкретного термина.
  Нейросети обычно ищут кириллицей, конкретные продукты — латиницей.
- Не использовать спам-теги типа #подписка #лайк #репост #взаимка.
- Не придумывать теги, которых в ВК никто не использует.
  Ориентир: тег живой, если он описывает реальную тему,
  а не искусственно склеен под один пост.

### Запрещённые теги
- #подпишись #взаимныеподписки #лайкзалайк #топ #реклама
- Теги-простыни из 15+ слов
- Теги с оскорблениями, запрещёнкой, политикой

# Ограничения
- Длина основного текста: исходная ±20% (блок хэштегов не считается).
- Не добавлять эмодзи в тело поста, если их не было в оригинале.
- Не добавлять свой CTA, если его не было в оригинале.
- Не добавлять абзацев с новыми фактами.

# Самопроверка перед выдачей
1. Первые 2-3 строки работают как самостоятельный хук?
2. В тексте нет markdown-разметки?
3. Все цифры, имена, названия из оригинала на месте?
4. Выброшены слова-паразиты из Шага 5?
5. Длина в пределах ±20%?
6. Блок хэштегов в конце, 5-8 штук, структура 1+2-3+2-3?
7. Фирменный тег на первом месте (если задан)?
8. Нет спам-тегов и придуманных тегов?
9. Ответ не начинается с "Конечно" / "Вот" / "Готово"?

# Напоминание
Выдай текст поста для ВК, затем пустую строку, затем блок
хэштегов. Без преамбулы, без пояснений после."""


async def adapt_vk(text: str) -> str:
    """Adapt text for VK style using Gemini with OpenAI fallback."""
    prompt = VK_SYSTEM_PROMPT.replace("{tg_post}", text)
    if GEMINI_API_KEY:
        try:
            async with httpx.AsyncClient() as client:
                r = await client.post(
                    f"https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash-lite:generateContent?key={GEMINI_API_KEY}",
                    json={
                        "contents": [{
                            "parts": [{"text": prompt}]
                        }],
                        "generationConfig": {"temperature": 1.0}
                    },
                    timeout=30
                )
                return r.json()["candidates"][0]["content"]["parts"][0]["text"]
        except Exception as e:
            print(f"Gemini error: {e}, falling back to OpenAI")

    # OpenAI fallback
    async with httpx.AsyncClient() as client:
        r = await client.post(
            "https://api.openai.com/v1/chat/completions",
            headers={"Authorization": f"Bearer {OPENAI_API_KEY}"},
            json={
                "model": "gpt-4.1-mini",
                "messages": [
                    {"role": "system", "content": prompt}
                ],
                "temperature": 1.0
            },
            timeout=30
        )
        return r.json()["choices"][0]["message"]["content"]


async def adapt_dzen(text: str, keywords: list) -> str:
    """Adapt text for Dzen with SEO."""
    keyword_strings = []
    for kw in keywords[:20]:
        if isinstance(kw, dict):
            keyword_strings.append(kw.get("phrase", str(kw)))
        else:
            keyword_strings.append(str(kw))
    keywords_str = ", ".join(keyword_strings)
    async with httpx.AsyncClient() as client:
        r = await client.post(
            f"https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash:generateContent?key={GEMINI_API_KEY}",
            json={
                "contents": [{
                    "parts": [{
                        "text": (
                            f"Напиши вирусный пост для Яндекс.Дзен в HTML-формате на основе этого материала.\n"
                            f"SEO-ключевые слова для вставки: {keywords_str}\n"
                            f"Правила:\n"
                            f"- До 4000 символов\n"
                            f"- HTML теги: <p>, <b>, <ul>, <li>\n"
                            f"- Вирусный заголовок\n"
                            f"- Добавь ссылку на источник: https://t.me/+jhUtJ494uvtlYjhi\n"
                            f"Материал:\n{text}"
                        )
                    }]
                }],
                "generationConfig": {"temperature": 1.0}
            },
            timeout=45
        )
        return r.json()["candidates"][0]["content"]["parts"][0]["text"]


async def adapt_youtube(text: str) -> str:
    """Adapt text for YouTube description."""
    async with httpx.AsyncClient() as client:
        r = await client.post(
            "https://api.openai.com/v1/chat/completions",
            headers={"Authorization": f"Bearer {OPENAI_API_KEY}"},
            json={
                "model": "gpt-4.1-mini",
                "messages": [
                    {"role": "system", "content": (
                        "Адаптируй текст под описание YouTube-видео.\n"
                        "- До 200 слов\n"
                        "- Добавь CTA в конце\n"
                        "- Упомяни канал @leaduxAI\n"
                        "- Без html тегов и звёздочек"
                    )},
                    {"role": "user", "content": text}
                ],
                "temperature": 0.8
            },
            timeout=20
        )
        return r.json()["choices"][0]["message"]["content"]


